public class Ajuda {
    private Voluntário vol;
    private int id;

    public Ajuda(int id){
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setVol(Voluntário vol) {
        this.vol = vol;
    }

    public Voluntário getVol() {
        return vol;
    }


}
